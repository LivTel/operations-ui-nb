    private JTable createCatalogTable() {
        ctm = new CatalogTableModel();
        ctr = new CatalogTableRenderer();

        JTable table = new JTable(ctm);

        int nc = table.getColumnModel().getColumnCount();
        for (int ic = 0; ic < nc; ic++) {
            table.getColumnModel().getColumn(ic).setCellRenderer(ctr);
        }

        return table;
    }

    private Action createSaveAction() {

        AbstractAction action = new AbstractAction() {

            @Override
            public void actionPerformed(ActionEvent ae) {

                useCatName = jTextField1.getText().trim();
                CatalogDisplayDescriptor cdd = new CatalogDisplayDescriptor(useCatName);
                cdd.setName(useCatName);
                cdd.setColor(useColor);
                cdd.setSymbol(useSymbol);
                cdd.setShowLabel(jCheckBox2.isSelected());
                cdd.setShowSymbol(jCheckBox1.isSelected());
                cdd.setShowAll(jCheckBox3.isSelected());

                if (!cdd.isShowAll()) {
                    int showCount = (Integer) jSpinner1.getValue();
                    cdd.setShowCount(showCount);
                }

                CatalogDisplay display = new CatalogDisplay(catalog, cdd);

                if (edit) {

                    try {
                        trackingPerspective.updateCatalogDisplay(display);
                    } catch (Exception e) {
                        e.printStackTrace();
                    }
                } else {

                    catalog.setCatalogName(useCatName);

                    try {
                        trackingPerspective.addCatalogDisplay(display);
                    } catch (Exception e) {
                        e.printStackTrace();
                    }

                }

                dlg.dispose();

            }
        };
        return action;

    }

