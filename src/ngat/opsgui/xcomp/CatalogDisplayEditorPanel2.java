    private Icon makeSymbolIcon(int symbol) {

        final int fsymbol = symbol;

        Icon icon = new Icon() {

            @Override
            public void paintIcon(Component c, Graphics g, int x, int y) {
                CatalogDisplayDescriptor cd = new CatalogDisplayDescriptor(null);
                cd.setSymbol(fsymbol);
                cd.setColor(Color.BLUE);
                cd.setShowSymbol(true);
                cd.paintComponent(g, "", false, x + 8, y + 8);
            }

            @Override
            public int getIconWidth() {
                return 16;
            }

            @Override
            public int getIconHeight() {
                return 16;
            }

        };

        return icon;
    }
