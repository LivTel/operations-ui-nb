    public void editCatalogDisplay(CatalogDisplayDescriptor cdd) {
        //ctm.setRows(catalog);
        edit = true;

        jTextField1.setText(catalog.getCatalogName());
        jTextField1.setEditable(false);

        useColor = cdd.getColor();
        useSymbol = cdd.getSymbol();
        jCheckBox1.setSelected(cdd.isShowSymbol());
        jCheckBox2.setSelected(cdd.isShowLabel());

        if (cdd.isShowAll()) {
            jCheckBox3.setSelected(true);
            jSpinner1.getModel().setValue(1);
            jSpinner1.setEnabled(false);
        } else {
            jCheckBox3.setSelected(false);
            jSpinner1.getModel().setValue(cdd.getShowCount());
            jSpinner1.setEnabled(true);
        }
        dlg = new JDialog();
        dlg.getContentPane().add(this);
        dlg.pack();
        dlg.setVisible(true);
    }

